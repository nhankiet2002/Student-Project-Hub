function id(prefix: string, n: number) {
  return `${prefix}_${String(n).padStart(3, "0")}`;
}

const now = new Date();
function daysFromNow(d: number) {
  return new Date(now.getTime() + d * 86400000).toISOString();
}
function daysAgo(d: number) {
  return new Date(now.getTime() - d * 86400000).toISOString();
}

